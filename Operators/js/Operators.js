"use strict"

//**Arithmetic operations */

var dataA = 10;
var dataB = 20;

// SUM +
var sum = dataA + dataB;
console.log(dataA + ' + ' + dataB + ' is equal to: ', sum)

// MINUS -
var minus = dataA - dataB;
console.log(dataA + ' - ' + dataB + ' is equal to: ', minus)

// MULTIPLY *
var multiply = dataA * dataB;
console.log(dataA + ' * ' + dataB + ' is equal to: ', multiply)

// DIVISION /
var division = dataA / dataB;
console.log(dataA + ' / ' + dataB + ' is equal to: ', division)

// MODULO %
var module = dataA % dataB;
console.log(dataA + ' % ' + dataB + ' is equal to: ', module)

// INCREMENT ++
var increment = dataA;
increment++;
console.log('Increment of ' + dataA + ' is: ', increment)

// DECREMENT --
var decrement = dataA;
decrement--;
console.log('Decrement of ' + dataA + ' is: ', decrement)