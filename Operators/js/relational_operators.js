"use strict"

var dataA = 10;
var dataB = 20;

//Bigger than >
var biggerThan = dataA > dataB;
console.log('Is ' + dataA + ' bigger than ' + dataB + '? ', biggerThan)

//Less than <
var lessThan = dataA < dataB;
console.log('Is ' + dataA + ' less than ' + dataB + '? ', lessThan)

//Bigger than or equal to >=
var biggerThanOrEqual = dataA >= dataB;
console.log('Is ' + dataA + ' bigger or equal to ' + dataB + '? ', biggerThanOrEqual)

//Less than or equal to <=
var lessThanOrEqual = dataA <= dataB;
console.log('Is ' + dataA + ' less or equal to ' + dataB + '? ', lessThanOrEqual)

//Equal ==
var equal = dataA == dataB;
console.log('Is ' + dataA + ' equal to ' + dataB + '? ', equal)

//Not equal !=
var notEqual = dataA != dataB;
console.log('Is ' + dataA + ' not equal to ' + dataB + '? ', notEqual)