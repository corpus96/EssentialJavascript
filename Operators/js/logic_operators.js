"use strict"

//Logic operators

var dataA = 10;
var dataB = 20;

//AND operator && 
var and = (dataA > 10 && dataB > 10);
console.log('AND evaluation is: ', and)

//OR operator ||
var or = (dataA > 10 || dataB > 10);
console.log("OR evaluation is: ", or)

//NOT operator !
var not = !(dataA > 10);
console.log("NOT evaluation is: ", not)
